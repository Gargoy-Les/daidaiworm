BOOL _OutCharXY(long X, long Y, char ASC)
{
	long 	x, y, offset;
	BYTE 	mask;

	if(ASC < 32) return FALSE;
	offset = (ASC - 32 ) << 3;
	for(y = 0; y < 8; y ++)
	{
		mask=SYSFONT[offset+y];
		for(x = 0; x < 6; x ++)
		{
			if(mask > 0x7F)
			{
				_PSet(X + x, Y + y);
			}
			mask <<= 1;
		}
	}
	return TRUE;
}

